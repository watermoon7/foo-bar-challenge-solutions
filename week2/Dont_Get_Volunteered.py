def solution(src, dest):
    src = [src%8, src//8]
    dest = [dest%8, dest//8]

   
    if src == dest:
        return 0
   
    diff = [abs(src[0] - dest[0]), abs(src[1] - dest[1])]
   
    moves = {
         1: [[2, 1],[1, 2]],
         2: [[1, 1],[0, 2],[2, 0],[1, 3],[3, 1],[0, 4],[4, 0],[4, 2],[2, 4],[3, 3]],
         3: [[2, 3],[3, 2],[0, 1],[1, 0],[0, 3],[3, 0],[0, 5],[5, 0],[1, 4],[4, 1],[4, 3],[3, 4],[6, 1],[1, 6],[2, 5],[5, 2],[6, 3],[3, 6],[5, 4],[4, 5]],
         4: [[2, 2],[0, 6],[6, 0],[4, 4],[5, 1],[1, 5],[6, 2],[2, 6],[5, 3],[3, 5],[7, 1],[1, 7],[7, 3],[3,7],[7, 5],[5, 7],[5,5],[4,6],[6,6],[6,4]],
         5: [[7, 0],[0, 7],[7, 2],[2, 7],[7, 4],[4, 7],[7, 6],[6, 7],[6, 5],[5, 6]],
         6: [[7, 7]]
         }

    for o, i in enumerate(moves.values()):
        if diff in i:
            if src in [[7,7],[0,7],[7,0],[0,0]]:
                if diff == [1,1]:
                    return 4
            return o+1
